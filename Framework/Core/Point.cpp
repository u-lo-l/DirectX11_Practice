#include "Framework.h"
#include "Point.h"

//////////////////////////////////////////////////////////////////////////

const Point Point::Zero = Point(0, 0);

//////////////////////////////////////////////////////////////////////////


//////////////////////////////////////////////////////////////////////////
///@brief 생성자
//////////////////////////////////////////////////////////////////////////
Point::Point(void)
{
	X = 0;
	Y = 0;
}
//////////////////////////////////////////////////////////////////////////


//////////////////////////////////////////////////////////////////////////
///@brief 생성자
///@param x : X
///@param y : Y
//////////////////////////////////////////////////////////////////////////
Point::Point(int x, int y)
{
	X = x;
	Y = y;
}
//////////////////////////////////////////////////////////////////////////


//////////////////////////////////////////////////////////////////////////
///@brief ==
///@param value2 : 포인트
///@return 결과
//////////////////////////////////////////////////////////////////////////
bool Point::operator==(const Point& value2) const
{
	if (X == value2.X)
		return Y == value2.Y;
	else
		return false;
}
//////////////////////////////////////////////////////////////////////////


//////////////////////////////////////////////////////////////////////////
///@brief !=
///@param value2 : 포인트
///@return 결과
//////////////////////////////////////////////////////////////////////////
bool Point::operator!=(const Point& value2) const
{
	if (X == value2.X)
		return Y != value2.Y;
	else
		return true;
}


D3DXVECTOR2 Point::ToDX()
{
	return D3DXVECTOR2((float)X, (float)Y);
}

Vector2D Point::ToVector2()
{
	return Vector2D((float)X, (float)Y);
}
//////////////////////////////////////////////////////////////////////////


//////////////////////////////////////////////////////////////////////////
///@brief 문자열로 변환
///@param 문자열
//////////////////////////////////////////////////////////////////////////
std::wstring Point::ToString()
{
	std::wstring temp = L"";

	temp += L"X:" + std::to_wstring(X);
	temp += L",Y:" + std::to_wstring(Y);

	return temp;
}